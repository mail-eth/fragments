// Utility function to get daily quote based on date
export function getDailyQuote() {
  const today = new Date();
  const dayOfYear = Math.floor((today - new Date(today.getFullYear(), 0, 0)) / 86400000);
  const quoteIndex = dayOfYear % dailyQuotes.length;
  return dailyQuotes[quoteIndex];
}

// Function to check if quote was already shown today
export function shouldShowDailyQuote() {
  const today = new Date().toDateString();
  const lastShown = localStorage.getItem('lastQuoteShown');
  return lastShown !== today;
}

// Mark quote as shown for today
export function markQuoteAsShown() {
  const today = new Date().toDateString();
  localStorage.setItem('lastQuoteShown', today);
}